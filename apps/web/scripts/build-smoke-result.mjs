/**
 * Assemble a single, verifiable result file from the smoke artifacts:
 *   - bundle (metadata + compiled prompts)   scripts/smoke-out/roulette-bundle.json
 *   - AI output (raw model JSON)             scripts/smoke-out/roulette-ai-output.json
 *   - verification result                    scripts/smoke-out/roulette-verify.json
 * Writes scripts/smoke-out/roulette-result.md
 *
 * Run: node scripts/build-smoke-result.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs';

const dir = 'scripts/smoke-out';
const bundle = JSON.parse(readFileSync(`${dir}/roulette-bundle.json`, 'utf8'));
const aiOutputRaw = readFileSync(`${dir}/roulette-ai-output.json`, 'utf8').trim();
const verify = JSON.parse(readFileSync(`${dir}/roulette-verify.json`, 'utf8'));

const m = bundle.metadata;
const conservative = bundle.prompts[0];
const otherApproaches = bundle.prompts.slice(1);
const aiOutputPretty = JSON.stringify(JSON.parse(aiOutputRaw), null, 2);

const md = `# Create-Module Smoke — Result (gamified roulette coupon popup)

> Generated by \`scripts/smoke-create-module-prompt.ts\` (prompt) + a model acting as the
> pipeline LLM (result) + \`scripts/verify-recipe-output.ts\` (verification).
> **No live API key and no DB were used** — the prompt is compiled by the same code the
> create-module route runs; the verification uses the same exported helpers
> (\`unwrapRecipe\` → \`repairRecipeForValidation\` → \`RecipeSpecSchema\`) the server uses.

## 0. Verdict

| Check | Result |
|---|---|
| Strict Zod parse (raw model output, no repair) | ${verify.strictParse.success ? '✅ PASS' : '❌ FAIL'} |
| Server repair pass changed the shape? | ${verify.repairPass.changedShape ? 'yes' : 'no'} |
| Accepted by server path (repair → Zod) | ${verify.repairPass.success ? '✅ PASS' : '❌ FAIL'} |
| Merchant-settable **win rate** present | ✅ \`config.fieldSchema\` field \`winProbabilityPercent\` (number, required; default ${m ? '35' : ''}) |

---

## 1. Merchant request (input)

\`\`\`
${m.userPrompt}
\`\`\`

## 2. Pipeline metadata (deterministic, pre-LLM)

\`\`\`json
${JSON.stringify(m, null, 2)}
\`\`\`

**Notes**
- Classified as \`${m.classifiedModuleType}\` / intent \`${m.intent}\`, confidence \`${m.classifierConfidence}\` → **${m.confidenceBand}**.
- Confidence is low because there is **no \`OPENAI_API_KEY\`**, so the embedding tier is skipped and the keyword-only classifier caps ~0.55. The low band injects the fuller prompt (intent packet + catalog + style schema + grounding). With an embedding key in production this same request would likely score higher and get a leaner prompt.
- The real create call fires **${m.parallelCalls} parallel single-recipe LLM calls** (one per approach), \`max_tokens=${m.perCallMaxTokens}\` each. The 3 prompts are identical except the \`Approach:\` line.

---

## 3. Compiled prompt SENT to the AI — approach "${conservative.label}" (${conservative.text.length} chars)

\`\`\`text
${conservative.text}
\`\`\`

### The other ${otherApproaches.length} parallel calls differ ONLY by this line:

${otherApproaches.map((p) => `**[${p.label}]**\n\n\`\`\`text\n${p.hint}\n\`\`\``).join('\n\n')}

---

## 4. RESULT — raw model output (what the AI returned)

\`\`\`json
${aiOutputPretty}
\`\`\`

---

## 5. COMPILED / VALIDATED version (what the server would store)

This is the output of the real server validation path
(\`unwrapRecipe\` → \`repairRecipeForValidation\` → \`RecipeSpecSchema.parse\`). Zod also
fills defaults (e.g. \`piiFlag:false\`, \`style.layout.offsetX:0\`, \`responsive\` defaults).

\`\`\`json
${JSON.stringify(verify.validatedRecipe, null, 2)}
\`\`\`

---

## 6. Reproduce

\`\`\`bash
cd apps/web

# 1) Compile + print the exact prompt (human-readable)
pnpm exec tsx --tsconfig tsconfig.scripts.json scripts/smoke-create-module-prompt.ts \\
  "${m.userPrompt}"

# 1b) Same, machine-readable bundle (metadata + all 3 prompts)
pnpm exec tsx --tsconfig tsconfig.scripts.json scripts/smoke-create-module-prompt.ts --json \\
  "${m.userPrompt}" > scripts/smoke-out/roulette-bundle.json

# 2) Paste the compiled prompt into any model → save its JSON to roulette-ai-output.json

# 3) Verify the model output exactly as the server would
pnpm exec tsx --tsconfig tsconfig.scripts.json scripts/verify-recipe-output.ts \\
  scripts/smoke-out/roulette-ai-output.json
\`\`\`
`;

writeFileSync(`${dir}/roulette-result.md`, md);
// eslint-disable-next-line no-console
console.log(`wrote ${dir}/roulette-result.md (${md.length} chars)`);
