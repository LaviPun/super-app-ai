import type { CompileResult } from './types';
import type { RecipeSpec } from '@superapp/core';

const SECTION_KEY_PREFIX = 'sections/superapp-notification-bar-';

export function compileNotificationBar(spec: Extract<RecipeSpec, { type: 'theme.notificationBar' }>, themeId: string): CompileResult {
  const slugged = slug(spec.name);
  const sectionKey = `${SECTION_KEY_PREFIX}${slugged}.liquid`;
  const cssFile = `superapp-notification-bar-${slugged}.css`;
  const cssKey = `assets/${cssFile}`;

  const liquid = `
{% comment %} Generated by SuperApp. Do not edit directly. {% endcomment %}
{{ '${cssFile}' | asset_url | stylesheet_tag }}
<div class="superapp-note" data-dismissible="${spec.config.dismissible ? '1' : '0'}">
  <div class="superapp-note__inner">
    <span class="superapp-note__msg">${escapeHtml(spec.config.message)}</span>
    ${
      spec.config.linkText && spec.config.linkUrl
        ? `<a class="superapp-note__link" href="${escapeHtml(spec.config.linkUrl)}">${escapeHtml(spec.config.linkText)}</a>`
        : ''
    }
    ${
      spec.config.dismissible
        ? `<button class="superapp-note__close" type="button" aria-label="Dismiss">×</button>`
        : ''
    }
  </div>
</div>

<script>
(function(){
  var el = document.currentScript && document.currentScript.previousElementSibling;
  if(!el) return;
  var close = el.querySelector('.superapp-note__close');
  if(!close) return;
  var key = 'superapp_note_' + ${JSON.stringify(slugged)};
  try{ if(localStorage.getItem(key) === '1'){ el.remove(); return; } }catch(e){}
  close.addEventListener('click', function(){
    try{ localStorage.setItem(key, '1'); }catch(e){}
    el.remove();
  });
})();
</script>

{% schema %}
{
  "name": "${escapeJson(spec.name)}",
  "settings": [],
  "presets": [{ "name": "${escapeJson(spec.name)}" }]
}
{% endschema %}
`.trim();

  const css = `
.superapp-note { position: sticky; top: 0; z-index: 30; background: #111; color: #fff; }
.superapp-note__inner { display:flex; gap: 10px; align-items:center; justify-content:center; padding: 10px 12px; }
.superapp-note__link { color: #fff; text-decoration: underline; }
.superapp-note__close { margin-left: 8px; border: 0; background: transparent; color: inherit; font-size: 18px; cursor: pointer; }
`.trim();

  return {
    ops: [
      { kind: 'THEME_ASSET_UPSERT', themeId, key: sectionKey, value: liquid },
      { kind: 'THEME_ASSET_UPSERT', themeId, key: cssKey, value: css },
      { kind: 'AUDIT', action: 'compile.theme.notificationBar', details: JSON.stringify({ sectionKey, cssKey }) },
    ],
    compiledJson: JSON.stringify({ sectionKey, cssKey }),
  };
}

function slug(input: string) {
  return input.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '').slice(0, 40);
}

function escapeJson(input: string) {
  return input.replace(/\\/g, '\\\\').replace(/"/g, '\"');
}

function escapeHtml(input: string) {
  return input
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}
